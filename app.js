let chatHistory = [];

// DOM要素
const chatLog = document.getElementById('chat-log');
const userInput = document.getElementById('user-input');
const btnSend = document.getElementById('btn-send');
const apiKeyInput = document.getElementById('api-key');
const progressView = document.getElementById('progress-view');

// ローカルストレージからAPIキーと状態の復旧
if (localStorage.getItem('gemini_api_key')) {
  apiKeyInput.value = localStorage.getItem('gemini_api_key');
}

// メッセージ表示関数
function appendMessage(role, text) {
  const msgDiv = document.createElement('div');
  msgDiv.classList.add('message', role);
  msgDiv.textContent = text;
  chatLog.appendChild(msgDiv);
  chatLog.scrollTop = chatLog.scrollHeight;
}

// ステータスUIの更新
function updateStatusUI() {
  progressView.textContent = JSON.stringify(sessionData.Progress, null, 2);
}

// Gemini API呼出コアロジック
async function callGeminiAPI(promptText) {
  const apiKey = apiKeyInput.value.trim();
  if (!apiKey) {
    alert("Gemini APIキーを入力してください。");
    return null;
  }
  localStorage.setItem('gemini_api_key', apiKey);

  // ユーザー入力を履歴に追加
  chatHistory.push({ role: "user", parts: [{ text: promptText }] });

  // //SYSTEMコマンドのフロントエンド側での検知
  const isSystemCommand = promptText.startsWith("//SYSTEM");

  // APIエンドポイント設定 (gemini-2.5-flashを使用)
  const url = `https://generativelanguage.googleapis.com/v1beta/models/gemini-3.5-flash:generateContent?key=${apiKey}`;

  const requestBody = {
    contents: chatHistory,
    systemInstruction: {
      parts: [{ text: BASE_SYSTEM_INSTRUCTION + `\n現在のセッションデータ:\n${JSON.stringify(sessionData)}` }]
    },
    generationConfig: {
      temperature: 0.7,
      maxOutputTokens: 1500
    }
  };

  try {
    const response = await fetch(url, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(requestBody)
    });

    const data = await response.json();
    if (data.candidates && data.candidates[0].content) {
      const aiResponse = data.candidates[0].content.parts[0].text;
      
      // AIの応答を履歴に追加
      chatHistory.push({ role: "model", parts: [{ text: aiResponse }] });

      // セーブデータ（JSONコードブロック）が含まれているか解析して内部状態を同期
      parseJsonFromResponse(aiResponse);

      return { text: aiResponse, type: isSystemCommand ? 'system' : 'assistant' };
    } else {
      throw new Error(data.error ? data.error.message : "意図しないAPI応答フォーマットです。");
    }
  } catch (error) {
    console.error(error);
    return { text: `エラーが発生しました: ${error.message}`, type: 'system' };
  }
}

// 応答テキストからJSONセーブデータを抽出してパース
function parseJsonFromResponse(text) {
  const jsonRegex = /\{[\s\S]*"AETHERIUM_SAVE"\s*:\s*true[\s\S]*\}/;
  const match = text.match(jsonRegex);
  if (match) {
    try {
      const parsed = JSON.parse(match[0]);
      sessionData = parsed;
      updateStatusUI();
    } catch (e) {
      console.warn("セーブデータの自動同期パースに失敗しました", e);
    }
  }
}

// 送信イベントハンドラ
async function handleSend() {
  const text = userInput.value.trim();
  if (!text) return;

  appendMessage('user', text);
  userInput.value = '';

  const res = await callGeminiAPI(text);
  if (res) {
    appendMessage(res.type, res.text);
  }
}

btnSend.addEventListener('click', handleSend);
userInput.addEventListener('keydown', (e) => {
  if (e.key === 'Enter' && !e.shiftKey) {
    e.preventDefault();
    handleSend();
  }
});

// 各種システムボタンの挙動設定
document.getElementById('btn-save').addEventListener('click', async () => {
  appendMessage('user', '//SYSTEM: SAVE');
  const res = await callGeminiAPI('//SYSTEM: SAVE');
  if (res) appendMessage(res.type, res.text);
});

document.getElementById('btn-load').addEventListener('click', () => {
  const rawJson = prompt("セーブデータ(JSON文字列)を貼り付けてください:");
  if (rawJson) {
    try {
      const parsed = JSON.parse(rawJson);
      if (parsed.AETHERIUM_SAVE) {
        sessionData = parsed;
        updateStatusUI();
        chatHistory = []; // 新規セッションとしてタイムラインを初期化
        appendMessage('system', "セーブデータが正常に読み込まれました。セッションを再開します。");
        callGeminiAPI("起動用トリガー: ロードが完了しました。進捗表とプロフィールを確認させてください。").then(res => {
          if (res) appendMessage(res.type, res.text);
        });
      } else {
        alert("有効なAetherium Teacherのセーブデータではありません。");
      }
    } catch (e) {
      alert("JSONのパースに失敗しました。");
    }
  }
});

